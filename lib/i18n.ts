export const LOCALE_COOKIE = "gofriends_locale";

export type Locale = "zh" | "en";

export function normalizeLocale(value: string | null | undefined): Locale {
  return value?.toLowerCase().startsWith("en") ? "en" : "zh";
}

export function localeFromCookieHeader(cookieHeader: string | null): Locale {
  const raw = cookieHeader
    ?.split(";")
    .map((part) => part.trim())
    .find((part) => part.startsWith(`${LOCALE_COOKIE}=`))
    ?.slice(LOCALE_COOKIE.length + 1);
  return normalizeLocale(raw);
}

export function getRequestLocale(request: Request): Locale {
  const explicit = request.headers.get("x-gofriends-locale");
  if (explicit) return normalizeLocale(explicit);
  const cookieLocale = localeFromCookieHeader(request.headers.get("cookie"));
  if (request.headers.get("cookie")?.includes(`${LOCALE_COOKIE}=`)) return cookieLocale;
  return normalizeLocale(request.headers.get("accept-language"));
}

export function formatDate(value: Date, locale: Locale) {
  return value.toLocaleDateString(locale === "en" ? "en-US" : "zh-CN");
}

